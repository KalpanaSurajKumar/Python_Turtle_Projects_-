import turtle

s = turtle.getscreen()
turtle.ht()

t1 = turtle.Turtle()
t2=turtle.Turtle()
t2.ht()

t1.pencolor("navy blue")


t1.pensize(2)
t1.circle(25)
t1.penup()
t1.goto(0,25)
t1.pendown()
t1.lt(90)

for i in range(24):
    t1.forward(25)
    t1.backward(25)
    t1.rt(15)

t1.penup()
t1.backward(25)
t1.lt(90)
t1.pendown()
t1.pencolor("green")
t1.fillcolor("green")
t1.begin_fill()
t1.forward(150)
t1.lt(90)
t1.forward(50)
t1.lt(90)
t1.forward(300)
t1.lt(90)
t1.forward(50)
t1.lt(90)
t1.forward(150)


t1.end_fill()
t1.ht()
t2.showturtle()
t2.pencolor("orange")
t2.fillcolor("orange")
t2.lt(90)
t2.penup()
t2.fd(50)
t2.lt(90)
t2.pendown()
t2.begin_fill()
t2.forward(150)
t2.rt(90)
t2.forward(50)
t2.rt(90)
t2.forward(300)
t2.rt(90)
t2.forward(50)
t2.rt(90)
t2.forward(150)
t2.end_fill()
t2.ht()
turtle.mainloop()
